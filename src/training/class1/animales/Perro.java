package training.class1.animales;

import training.class1.Tamaño;

public class Perro {
	int edad;
	int peso;
	boolean hembra;
	Tamaño tamaño;
	String nombre;
	
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	public boolean isHembra() {
		return hembra;
	}
	public void setHembra(boolean hembra) {
		this.hembra = hembra;
	}
	public Tamaño getTamaño() {
		return tamaño;
	}
	public void setTamaño(Tamaño tamaño) {
		this.tamaño = tamaño;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
}
